#include <iostream>
#include <cstdio>
using namespace std;
int main(int argc, char const *argv[])
{
	int n;
	while(~scanf("%d", &n))
	{
		switch(n)
		{
			case 1:printf("5\n");break;
			case 2:printf("6\n");break;
			case 3:printf("7\n");break;
			case 4:printf("18\n");break;
			case 5:printf("1\n");break;
			case 6:printf("2\n");break;
		}
	}
	return 0;
}